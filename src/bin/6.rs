/**
 * (1+2+3+4+5+6+..)^2 = 1^2 + 2^2 + 3^2 + ...
 *                     + 2(1*2 + 1*3 + 1*4 + 1*5 + 1*6 + ..)
 *                     + 2(2*3 + 2*4 + 2*5 + 2*6 + ..)
 *                     + 2(3*4 + 3*5 + 3*6 + ..)
 *                     + ..
*/

fn main() {
    let mut sum = 0;
    let until = 100;

    for i in 1..until {
        for j in (i+1)..(until + 1) {
            println!("{}*{}", i, j);
            sum += i * j
        }
    }

    println!("{}", 2 * sum);
}
